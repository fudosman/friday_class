const os = require('os');

// Example usage of os.platform()
console.log('Platform:', os.platform());

// Example usage of os.arch()
console.log('Architecture:', os.arch());

// Example usage of os.hostname()
console.log('Hostname:', os.hostname());

// Example usage of os.cpus()
console.log('CPU Cores:', os.cpus());

// Example usage of os.totalmem()
console.log('Total Memory:', os.totalmem() + ' bytes');

// Example usage of os.freemem()
console.log('Free Memory:', os.freemem() + ' bytes');

// Example usage of os.homedir()
console.log('Home Directory:', os.homedir());

// Example usage of os.tmpdir()
console.log('Temporary Directory:', os.tmpdir());

// Example usage of os.networkInterfaces()
console.log('Network Interfaces:', os.networkInterfaces());

// Example usage of os.endianness()
console.log('Endianness:', os.endianness());

// Example usage of os.constants
console.log('Constants:', os.constants);
